//7 Primitative Data Types

// 1 Number - used for decimals and integers

// 2 String - a sequence of characters used for text

// 3 Boolean - true or false - logical values

true;
console.log(true);

let javascriptIsFun = true;
console.log(javascriptIsFun);

console.log(typeof true);
console.log(typeof javascriptIsFun);
console.log(typeof 23);
console.log(typeof 'hello');

javascriptIsFun = 'yes';  // dynamic typing to change the variable
console.log(typeof javascriptIsFun);

let year;
console.log(year);
console.log(typeof year);

year = 2020;

console.log(year);
console.log(typeof year);

console.log(typeof null);

// 4 Undefined - value taken by a variable that is not yet defined 

// 5 Null - value that represents nothing

// 6 Symbol - unique value that is not a string or number - cannot be changed

// 7 BigInt - used for large numbers

//Dynamic typing - do not have to manually define the data type of the value stored in a variable. Instead data types are determined automatically based on the value being assigned to the variable.